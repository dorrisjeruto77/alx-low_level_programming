#include <stdio.h>
/**
* add - add two integers
* @a: first int
* @b: second int
*Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - substract two integers
* @a: first int
* @b: second int
*Return: subtraction
*/
int sub(int a, int b)
{
        return (a - b);
}

/**
* div - divide two integers
* @a: first int
* @b: second int
*Return: division result
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - mod of two integers
* @a: first int
* @b: second int
* Return: final result
*/
int mod(int a, int b)
{
	return (a % b);
}

/**
* mul - multiply  two integers
* @a: first int
* @b: second int
*Return: multiply
*/
int multiply(int a, int b)
{
	return (a * b);
}
